package com.larrykin.snaptap.enums;

/**
 * Enum representing the types of actions that can be associated with a hotkey.
 * Each action type corresponds to a specific functionality:
 * - URL: Opens a web address in the default browser.
 * - APPLICATION: Launches an application.
 * - FILE_FOLDER: Opens a file or folder.
 */
public enum ActionType {
    URL, APPLICATION, FILE_FOLDER
}
